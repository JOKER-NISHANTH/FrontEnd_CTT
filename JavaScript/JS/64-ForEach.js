let Games = ["Soccer", "Football", "Hockey", "BasketBall", "Volleyball"];

Games.forEach(function (game,index ){
    console.log(`${index +1}-${game}`);
});

let Days = ['Mon', "Tues", "Wen", "Thrus", "Fri", "Sat", "Sun"];

Days.forEach(function (day,index) {
    console.log(`${index+1} ${day}`);
});